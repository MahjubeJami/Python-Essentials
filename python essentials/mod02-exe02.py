# 2.Assume that we execute the following assignment statements
# length = 10.0 , width = 7
# For each of the following expressions, write the value of
# the expression and the type (of the value of the expression).
#     width//2
#     length/2.0
#     length/2
#     1 + 4 * 5


length = 10.0
width = 7

width = width//2
print("width is", width , type(width))

length = length/2.0
print("length is", length , type(length))

len = length/2
print("length is", len , type(len))

total = 1 + 4 * 5
print("total is", total , type(total))
